/*=============== Typing-effect at home section ===============*/
var typeText = document.querySelector(".typeText")
var textToBeTyped = "a UI/UX Designer & \nFull-Stack Developer \nbased in Singapore."
var index = 0, isAdding = true

function playAnim() {
  setTimeout(function () {
    // set the text of typeText to a substring of
    // the textToBeTyped using index.
    typeText.innerText = textToBeTyped.slice(0, index)
    if (isAdding) {
      // adding text
      if (index > textToBeTyped.length) {
        // no more text to add
        isAdding = false
        //break: wait 2s before playing again
        setTimeout( function () {
          playAnim()
        }, 2000)
        return
      } else {
        // increment index by 1
        index++
      }
    }
    playAnim()
  }, 120)
}
// start animation
playAnim()


